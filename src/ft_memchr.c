#include "../include/libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t		i;
	const char	*str;

	i = 0;
	str = (const char *)s;
	while (i < n)
	{
		if (str[i] == (const char)c)
			return ((void *)(s + i));
		i++;
	}
	return (NULL);
}
